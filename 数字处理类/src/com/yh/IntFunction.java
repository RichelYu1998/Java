package com.yh;

public class IntFunction {
    public static void main(String[] args) {
        System.out.println("使用ceil()取整："+Math.ceil(5.2));
        System.out.println("使用floor()取整："+Math.floor(2.5));
        System.out.println("使用rint()取整："+Math.rint(2.7));
        System.out.println("使用rint()取整："+Math.rint(2.5));
        System.out.println("使用round()取整："+Math.round(3.4f));
        System.out.println("使用round()取整："+Math.round(2.5));
    }
}
